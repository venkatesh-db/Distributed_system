package main

import (
	"fmt"
	"log"
	"math/rand"
	"sync"
	"time"
)

//////////////////////////////////////////////////////
// DATABASE
//////////////////////////////////////////////////////

var database = map[string]string{}

var dbMutex sync.Mutex

//////////////////////////////////////////////////////
// CACHE
//////////////////////////////////////////////////////

var cache = map[string]string{}

var cacheMutex sync.RWMutex

//////////////////////////////////////////////////////
// STAMPEDE LOCK
//////////////////////////////////////////////////////

var stampedeLock sync.Mutex

//////////////////////////////////////////////////////
// WRITE THROUGH CACHE
//////////////////////////////////////////////////////

func writeThrough(
	key string,
	value string,
) {

	//////////////////////////////////////////////////
	// WRITE DB
	//////////////////////////////////////////////////

	dbMutex.Lock()

	database[key] = value

	dbMutex.Unlock()

	//////////////////////////////////////////////////
	// WRITE CACHE
	//////////////////////////////////////////////////

	cacheMutex.Lock()

	cache[key] = value

	cacheMutex.Unlock()

	log.Printf(
		"[WRITE-THROUGH] %s=%s",
		key,
		value,
	)
}

//////////////////////////////////////////////////////
// WRITE BEHIND CACHE
//////////////////////////////////////////////////////

func writeBehind(
	key string,
	value string,
) {

	//////////////////////////////////////////////////
	// UPDATE CACHE IMMEDIATELY
	//////////////////////////////////////////////////

	cacheMutex.Lock()

	cache[key] = value

	cacheMutex.Unlock()

	log.Printf(
		"[WRITE-BEHIND CACHE] %s=%s",
		key,
		value,
	)

	//////////////////////////////////////////////////
	// ASYNC DB WRITE
	//////////////////////////////////////////////////

	go func() {

		time.Sleep(2 * time.Second)

		dbMutex.Lock()

		database[key] = value

		dbMutex.Unlock()

		log.Printf(
			"[WRITE-BEHIND DB FLUSH] %s=%s",
			key,
			value,
		)
	}()
}

//////////////////////////////////////////////////////
// CACHE STAMPEDE PREVENTION
//////////////////////////////////////////////////////

func getProduct(
	key string,
) string {

	//////////////////////////////////////////////////
	// CACHE READ
	//////////////////////////////////////////////////

	cacheMutex.RLock()

	value, found := cache[key]

	cacheMutex.RUnlock()

	if found {

		log.Printf(
			"[CACHE HIT] %s",
			key,
		)

		return value
	}

	//////////////////////////////////////////////////
	// STAMPEDE PROTECTION
	//////////////////////////////////////////////////

	stampedeLock.Lock()

	defer stampedeLock.Unlock()

	//////////////////////////////////////////////////
	// DOUBLE CHECK
	//////////////////////////////////////////////////

	cacheMutex.RLock()

	value, found = cache[key]

	cacheMutex.RUnlock()

	if found {

		return value
	}

	log.Printf(
		"[CACHE MISS DB FETCH] %s",
		key,
	)

	//////////////////////////////////////////////////
	// SIMULATE SLOW DB
	//////////////////////////////////////////////////

	time.Sleep(3 * time.Second)

	dbMutex.Lock()

	value = database[key]

	dbMutex.Unlock()

	//////////////////////////////////////////////////
	// POPULATE CACHE
	//////////////////////////////////////////////////

	cacheMutex.Lock()

	cache[key] = value

	cacheMutex.Unlock()

	return value
}

//////////////////////////////////////////////////////
// CLIENT TRAFFIC
//////////////////////////////////////////////////////

func clients(
	clientID int,
) {

	for {

		productID := fmt.Sprintf(
			"product-%d",
			rand.Intn(5),
		)

		result := getProduct(productID)

		log.Printf(
			"[CLIENT-%d] RESULT=%s",
			clientID,
			result,
		)

		time.Sleep(
			time.Duration(rand.Intn(1000)) *
				time.Millisecond,
		)
	}
}

//////////////////////////////////////////////////////
// MAIN
//////////////////////////////////////////////////////

func main() {

	rand.Seed(time.Now().UnixNano())

	//////////////////////////////////////////////////
	// INITIAL DATA
	//////////////////////////////////////////////////

	writeThrough(
		"product-1",
		"iphone",
	)

	writeThrough(
		"product-2",
		"macbook",
	)

	writeBehind(
		"product-3",
		"ipad",
	)

	//////////////////////////////////////////////////
	// CLIENTS
	//////////////////////////////////////////////////

	for i := 1; i <= 10; i++ {

		go clients(i)
	}

	select {}
}
